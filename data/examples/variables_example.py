# Example: Python Variables

# Simple variable assignment
name = "Alice"
age = 25
height = 5.6
is_student = True

print(f"Name: {name}")
print(f"Age: {age}")
print(f"Height: {height} feet")
print(f"Student: {is_student}")

# Multiple assignment
x, y, z = 10, 20, 30
print(f"x={x}, y={y}, z={z}")

# Variable naming rules
# Good names:
user_name = "Bob"
total_count = 100
is_valid = True

# Type conversion
number_string = "42"
number_int = int(number_string)
print(f"Converted: {number_int}")
